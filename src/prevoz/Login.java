    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        LoginKlasa korisnik=new LoginKlasa();
        Login log=new Login();
        Prikaz p=new Prikaz();
        
        korisnik.setIme(jTextField1.getText());
        korisnik.setSifra(jPasswordField1.getText());
        
        
        
        if((korisnik.getIme().equals("luka")&&korisnik.getSifra().equals("kontrola1"))||
          (korisnik.getIme().equals("nikola")&&korisnik.getSifra().equals("kontrola2")))
        {
            
            log.setVisible(false);
            p.setVisible(true);
            dispose();
            
        }
        
        else
            jLabel3.setText("Nesipravno uneti podaci!");
            
    }//GEN-LAST:event_jButton2ActionPerformed

