
package prevoz;


public class LoginKlasa {
     private String ime;
     private String sifra;

    public LoginKlasa() {
    }

    public LoginKlasa(String ime, String sifra) {
        this.ime = ime;
        this.sifra = sifra;
    }

    public String getIme() {
        return ime;
    }

    public String getSifra() {
        return sifra;
    }

    public void setIme(String ime) {
        this.ime = ime;
    }

    public void setSifra(String sifra) {
        this.sifra = sifra;
    }
     
    
    
    
}
